package ru.netology.services;


public class FreelancerService {  // Объявляем публичный класс

    // Объявляем публичный метод calculate, который возвращает int и принимает 3 параметра
    public int calculate(int income, int expenses, int threshold) {

        // Шаг 1: Создаём переменную-счётчик для месяцев отдыха, начальное значение 0
        int count = 0;

        // Шаг 2: Создаём переменную для денег на счету, начальное значение 0
        int money = 0;

        // Шаг 3: Запускаем цикл на 12 месяцев (от 0 до 11)
        for (int month = 0; month < 12; month++) {

            // Шаг 4: Проверяем, хватает ли денег для отдыха
            if (money >= threshold) {

                // Шаг 4a: Если да - отдыхаем, увеличиваем счётчик на 1
                count++;

                // Шаг 4b: Вычитаем обязательные траты за месяц
                money = money - expenses;

                // Шаг 4c: Уменьшаем остаток в 3 раза (траты на отдых)
                money = money / 3;

            } else {

                // Шаг 5: Если денег не хватает - работаем
                // Добавляем доход и вычитаем обязательные траты
                money = money + income - expenses;
            }

            // Шаг 6: Цикл переходит к следующему месяцу
        }


        // Шаг 7: Возвращаем количество месяцев отдыха
        return count;
    }
}